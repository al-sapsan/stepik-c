#include <stdio.h>

// Определяем константы:
#define MAX_COUNTRIES 6    // Максимальное количество стран
#define MAX_NAME_LEN 30    // Максимальная длина названия страны

int main(void)
{
    // Объявляем двумерный массив для хранения стран:
    // 6 строк (стран) по 31 символу каждая (30 символов + '\0')
    char data[MAX_COUNTRIES][MAX_NAME_LEN + 1];

    // Считываем 6 названий стран:
    for (int i = 0; i < MAX_COUNTRIES; ++i)
        scanf("%30s", data[i]);  // "%30s" - читаем не более 30 символов

    // Флаг для отслеживания первого вывода (пробел выводится перед каждой страной, кроме первой)
    int first = 1;  // 1 - true, это первая страна на 'G'
    
    // Проверяем каждую страну:
    for (int i = 0; i < MAX_COUNTRIES; ++i) {
        // Если название начинается на 'G'
        if (data[i][0] == 'G') {
            // Если это не первая страна, выводим пробел перед названием
            if (!first)
                printf(" ");
            
            // Выводим название страны
            printf("%s", data[i]);
            
            // Устанавливаем флаг, что первая страна уже выведена
            first = 0;  // 0 - false
        }
    }
    
    // Переход на новую строку в конце
    printf("\n");

    return 0;
}