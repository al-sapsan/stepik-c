#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int main(void)
{
    srand(time(NULL)); // Инициализация генератора случайных чисел

    int a, b;
    // Считываем значения a и b
    if (scanf("%d %d", &a, &b) != 2 || a >= b) {
        printf("Input error.\n"); // Проверка корректности ввода
        return 0;
    }

    // Генерация и вывод 10 случайных вещественных чисел в диапазоне [a; b]
    for (int i = 0; i < 10; i++) {
        double random_number = ((double)rand() / RAND_MAX) * (b - a) + a; // Генерация числа в диапазоне [a; b]
        printf("%.2f ", random_number); // Вывод числа с точностью до сотых
    }
    printf("\n"); // Переход на новую строку после вывода чисел

    return 0;
}
/* Объяснение:
1. Считывание значений a и b:
- Программа считывает два целых числа a и b из входного потока.
- Проверяется, что a < b. Если условие не выполняется, выводится сообщение об ошибке.
2. Формула для генерации случайных вещественных чисел:
- rand() возвращает случайное целое число в диапазоне [0; RAND_MAX].
- (double)rand() / RAND_MAX нормализует это значение в диапазон [0; 1].
- Умножение на (b - a) масштабирует диапазон до [0; b - a].
- Добавление a сдвигает диапазон в [a; b].
3. Вывод чисел:
- Сгенерированные числа выводятся в одну строку через пробел.
- Используется спецификатор %.2f для вывода чисел с точностью до двух знаков после запятой.
*/