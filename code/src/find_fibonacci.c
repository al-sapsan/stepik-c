#include <stdio.h>

int main(void)
{
    int n; // Длина последовательности Фибоначчи
    scanf("%d", &n); // Считываем значение n

    // Проверяем, что n является натуральным числом
    if (n <= 0) {
        printf("Input error\n");
        return 0;
    }

    // Инициализируем первые два числа последовательности
    int a = 1, b = 1;
    int i = 1; // Счётчик для цикла

    // Выводим последовательность Фибоначчи с использованием цикла while
    while (i <= n) {
        if (i == 1) {
            printf("%d ", a); // Выводим первое число
        } else if (i == 2) {
            printf("%d ", b); // Выводим второе число
        } else {
            int next = a + b; // Следующее число — сумма двух предыдущих
            printf("%d ", next);
            a = b; // Обновляем предыдущее число
            b = next; // Обновляем текущее число
        }
        i++; // Увеличиваем счётчик
    }
    /*
    while (i <= n) {
        printf("%d ", (i == 1) ? a : 
                      (i == 2) ? b :
                      (a = b, b = a + b, a)); 
        i++;
    }
    */

    printf("\n"); // Переход на новую строку
    return 0;
}