#include <iostream>
using namespace std;

/**
 * @brief Вычисляет сумму цифр целого числа (рекурсивно)
 *
 * @param[in] n Целое число (может быть отрицательным)
 * @return int Сумма цифр числа (всегда положительная)
 *
 * @details Алгоритм работы:
 * 1. Для отрицательных чисел сначала преобразует их в положительные
 * 2. Базовый случай: если число состоит из одной цифры (n < 10), возвращает само число
 * 3. Рекурсивный случай: возвращает последнюю цифру (n % 10) плюс сумму цифр оставшегося числа (n / 10)
 *
 */
int sumOfDigits(int n)
{
    // Обработка отрицательных чисел
    if (n < 0)
    {
        return sumOfDigits(-n);
    }
    // Базовый случай: одна цифра
    if (n < 10)
    {
        return n;
    }
    // Рекурсивный случай: последняя цифра + сумма остальных
    return (n % 10) + sumOfDigits(n / 10);
}

int main()
{
    int n;
    cin >> n;
    cout << sumOfDigits(n);
    return 0;
}