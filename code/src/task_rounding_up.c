/* Задача:
Каждый день во время конференции расходуется N пакетиков чая. 
Конференция длится 7 дней. В пачке чая 100 пакетиков. 
Какого наименьшего количества пачек чая хватит на все дни конференции? 
Результат выведите в консоль в виде одного целого числа.
*/
#include <stdio.h>

int main(void)
{
    unsigned short N; // Количество пакетиков чая, расходуемых за день

    // Считываем значение N
    if (scanf("%hu", &N) != 1 || N == 0) {
        printf("Input error.\n"); // Проверка корректности ввода
        return 0;
    }

    // Вычисляем общее количество пакетиков чая, необходимое за 7 дней
    unsigned int total_tea_bags = N * 7;

    // Вычисляем минимальное количество пачек чая (каждая пачка содержит 100 пакетиков)
    unsigned int packs_needed = (total_tea_bags + 99) / 100; // Округление вверх

    // Выводим результат
    printf("%u\n", packs_needed);

    return 0;
}
/* Объяснение:
1. Считывание значения N:
- Программа считывает количество пакетиков чая, расходуемых за день.
- Проверяется, что N > 0. Если ввод некорректен, выводится сообщение об ошибке.
2. Вычисление общего количества пакетиков:
- Умножаем N на 7, чтобы получить общее количество пакетиков чая, необходимое за 7 дней.
3. Вычисление количества пачек:
- Каждая пачка содержит 100 пакетиков.
- Для округления вверх используется формула (total_tea_bags + 99) / 100. 
Это гарантирует, что даже если общее количество пакетиков не делится нацело на 100, 
результат будет округлен вверх.
4. Вывод результата:
- Результат (минимальное количество пачек) выводится в консоль.*/