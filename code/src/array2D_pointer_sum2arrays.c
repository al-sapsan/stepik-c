#include <stdio.h>

#define ROWS 3
#define COLS 4

int main(void)
{
    int a[ROWS][COLS] = {0};
    int b[ROWS][COLS] = {0};
    int res[ROWS][COLS] = {0};
    
    // Указатели для работы с массивами
    int *ptr_a = &a[0][0];
    int *ptr_b = &b[0][0];
    int *ptr_res = &res[0][0];
    
    // Ввод данных через указатели
    for(int i = 0; i < ROWS * COLS && scanf("%d", ptr_a) == 1; i++, ptr_a++);
    for(int i = 0; i < ROWS * COLS && scanf("%d", ptr_b) == 1; i++, ptr_b++);
    
    /* Сброс указателей на начало массивов, так как после ввода 
    указатели ptr_a и ptr_b указывают на концы массивов, поэтому 
    их нужно вернуть в начало для последующего сложения.*/
    ptr_a = &a[0][0];
    ptr_b = &b[0][0];
    
    // Сложение массивов через указатели
    for(int i = 0; i < ROWS * COLS; i++)
        *ptr_res++ = *ptr_a++ + *ptr_b++;
        /*На каждой итерации:
Берутся значения из текущих позиций ptr_a и ptr_b
Сумма записывается в текущую позицию ptr_res
Все три указателя перемещаются на следующие элементы*/
    
    // Сброс указателя результата
    ptr_res = &res[0][0];
    
    // Вывод результата через указатели
    for(int i = 0; i < ROWS; i++) {
        for(int j = 0; j < COLS; j++) {
            if(j > 0) printf(" "); // Пробел между элементами в строке
            printf("%d", *ptr_res++);
        }
        printf("\n"); // Переход на новую строку после каждой строки матрицы
    }

    return 0;
}