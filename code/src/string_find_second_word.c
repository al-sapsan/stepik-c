#include <stdio.h>

/* Функция удаляет символ новой строки (\n) в конце строки */
void strip_string(char* str, int max_len) {
    int count = 0;
    // Проходим по строке до конца или до достижения max_len
    while(*str++ != '\0' && count++ < max_len);

    // Если строка не пустая (длина > 1)
    if(count > 1) {
        str -= 2;  // Возвращаем указатель на предпоследний символ
        if(*str == '\n')
            *str = '\0';  // Заменяем \n на терминатор
    }
}

int main(void) {
    char str[100];  // Буфер для ввода строки
    
    // Безопасное чтение строки
    fgets(str, sizeof(str), stdin);
    // Удаление символа новой строки
    strip_string(str, sizeof(str));

    /* Поиск второго слова */
    int word_num = 0;    // Счетчик слов
    int in_word = 0;     // Флаг нахождения внутри слова
    int start = -1;      // Начальная позиция второго слова
    int end = -1;        // Конечная позиция второго слова

    // Проход по всем символам строки
    for (int i = 0; str[i] != '\0'; ++i) {
        // Если текущий символ не пробел и мы вне слова
        if (str[i] != ' ' && in_word == 0) {
            in_word = 1;
            word_num++;
            // Если это второе слово - запоминаем начало
            if (word_num == 2)
                start = i;
        } 
        // Если пробел и мы внутри слова
        else if (str[i] == ' ' && in_word == 1) {
            in_word = 0;
            // Если это конец второго слова - запоминаем позицию
            if (word_num == 2 && end == -1)
                end = i;
        }
    }

    /* Обработка результатов поиска */
    if (word_num < 2) {
        // Если слов меньше двух
        printf("no\n");
    } else {
        // Если второе слово последнее в строке
        if (end == -1) {
            // Вычисляем длину слова до конца строки
            end = 0; // начинаем отсчет длины слова с 0
            while (str[start + end] != '\0') ++end; // считаем количество символов до конца строки 
            end = start + end; // получаем диапазон от начала второго слова до конца строки
        }
        // Выводим второе слово посимвольно в соответствии с диапазоном
        // от start до end
        for (int i = start; i < end; ++i)
            putchar(str[i]);
        putchar('\n');
    }

    return 0;
}