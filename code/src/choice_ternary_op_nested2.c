#include <stdio.h>

int main(void)
{
    int a, b, c; // Переменные для хранения длин сторон треугольника

    // Считываем три целых числа через пробел
    if (scanf("%d %d %d", &a, &b, &c) != 3) {
        printf("Input error.\n"); // Если ввод некорректен
        return 0;
    }

    // Проверяем, могут ли числа быть сторонами треугольника
    const char *result = (a > 0 && b > 0 && c > 0) ? 
                         ((a + b > c && a + c > b && b + c > a) ? "yes" : "no") : 
                         "no";

    // Выводим результат
    printf("%s\n", result);

    return 0; // Успешное завершение программы
}