/* Задача:
Напишите программу чтения из входного потока вещественного значения 
угла, выраженного в градусах. Вычислите синус этого угла и выведите 
результат в консоль в виде вещественного значения с точностью до 
десятых.
P.S. Не забудьте перевести градусы в радианы по формуле: 
rad=grad⋅π/180. Значение π=3.1415.
*/
#include <stdio.h>
#include <math.h>

int main(void)
{
    double degrees; // Угол в градусах
    const double PI = 3.1415; // Значение π

    // Считываем значение угла в градусах
    if (scanf("%lf", &degrees) != 1) {
        printf("Input error.\n"); // Проверка корректности ввода
        return 0;
    }

    // Переводим градусы в радианы
    double radians = degrees * PI / 180.0;

    // Вычисляем синус угла
    double sine = sin(radians);

    // Выводим результат с точностью до десятых
    printf("%.1f\n", sine);

    return 0;
}
/*
Объяснение:
1. Считывание значения угла:
- Программа считывает вещественное значение угла в градусах (degrees) из входного потока.
- Используется формат %lf для считывания значения типа double.
2. Перевод в радианы:
- Формула radians = degrees * PI / 180.0 используется для перевода угла из градусов в радианы.
- Константа PI равна 3.1415.
3. Вычисление синуса:
- Функция sin() из библиотеки <math.h> используется для вычисления синуса угла, заданного в радианах.
4. Вывод результата:
- Результат выводится с точностью до одной десятой с помощью спецификатора %.1f.
*/