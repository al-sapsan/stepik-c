#include <stdio.h>

int main(void)
{
    float a, b, c; // Переменные для хранения трех вещественных чисел

    // Считываем три вещественных числа через запятую
    if (scanf("%f, %f, %f", &a, &b, &c) != 3) {
        printf("Input error.\n"); // Если ввод некорректен
        return 0;
    }

    // Определяем наименьшее число
    float min = a; // Предполагаем, что первое число минимальное
    if (b < min) {
        min = b; // Если второе число меньше, обновляем минимальное
    }
    if (c < min) {
        min = c; // Если третье число меньше, обновляем минимальное
    }

    // Выводим наименьшее число с точностью до сотых
    printf("%.2f\n", min);

    return 0; // Успешное завершение программы
}