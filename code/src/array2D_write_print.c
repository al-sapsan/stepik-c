/* Решение
1. Читаем 12 чисел через пробел
2. Помещаем в массив ar_2D 4х3
3. Выводим построчно элементы массива
* Необходимые структуры данных:
- массив для хранения чисел
- функция scanf для считывания чисел
- функция printf для вывода чисел
- вложенный цикл for для прохода по массиву
*/

#include <stdio.h>

int main(void) 
{
    int ar_2D[4][3] = {0};

    // Считываем 12 целых чисел и заполняем массив по порядку (по строкам)
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 3; ++j) {
            scanf("%d", &ar_2D[i][j]);
        }
    }
    // Выводим массив по столбцам (каждая строка вывода — это столбец массива)
    for (int j = 0; j < 3; ++j) {
        for (int i = 0; i < 4; ++i) {
            printf("%d ", ar_2D[i][j]);
        }
        printf("\n");
    }
    return 0;
}