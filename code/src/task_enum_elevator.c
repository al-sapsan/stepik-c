#include <stdio.h>

/**
 * @brief Симуляция лифта в торгово-развлекательном центре с использованием enum.
 *
 * Пользователь выбирает этаж (от 0 до 5), программа выводит описание этажа.
 * При вводе некорректного номера выводится сообщение об ошибке.
 *
 * @return 0 при успешном завершении
 */
int main() {
    // перечислимый тип для этажей
    enum level { PARKING, SUPERMARKET, HARDWARESTORES, BOUTIQUES, SPORTSPA, CLUBRESTAURANTBAR };
    int floor; // выбор этажа пользователем

    printf("\n\t$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$\n\n");
    printf("\tДобро пожаловать в наш торгово-развлекательный центр MALL!!!\n");
    printf("\tПредлагаем Вам проехаться в лифте и посетить все этажи!\n\n");
    printf("\t$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$\n\n");
    printf("\nНажмите кнопку с номером этажа (от 0 до 5): ");
    scanf("%d", &floor);

    switch (floor) {
        case PARKING:
            printf("\aВы спустились в паркинг!!!\n");
            break;
        case SUPERMARKET:
            printf("\aВы на первом этаже!\n");
            printf("Здесь вы можете посетить наш супермаркет и купить продукты и товары для дома.\n\n");
            break;
        case HARDWARESTORES:
            printf("\aВы на втором этаже!\n");
            printf("Здесь расположились магазины бытовой техники, IT и мобильных телефонов.\n\n");
            break;
        case BOUTIQUES:
            printf("\aВы на третьем этаже!\n");
            printf("Здесь вас ждет незабываемый шопинг! Одежда, обувь, магазины косметики.\n\n");
            break;
        case SPORTSPA:
            printf("\aВы на четвертом этаже!\n");
            printf("Здесь вы можете посетить бассейн, каток, спортзалы, spa-салон!\n\n");
            break;
        case CLUBRESTAURANTBAR:
            printf("\aВы на пятом этаже!\n");
            printf("Тут вы можете посетить ночной клуб, бар и ресторан!\n\n");
            break;
        default:
            printf("Ошибка! У нас только 5 этажей!\n\n");
    }
    return 0;
}