#include <stdio.h>

int main(void)
{
    short type = 0; // Переменная для хранения типа операции
    double w, h;    // Переменные для сторон прямоугольника

    // Считываем тип операции и стороны прямоугольника
    if (scanf("%hd %lf %lf", &type, &w, &h) != 3) {
        printf("Input error.\n"); // Если ввод некорректен
        return 0;
    }

    // Вычисляем результат в зависимости от значения type
    double result = (type == 1) ? (w * h) : 
                    (type == 2) ? (2 * (w + h)) : -1;

    // Выводим результат
    if (type == 1 || type == 2) {
        printf("%.1f\n", result); // Результат вычислений с точностью до десятых
    } else {
        printf("-1\n"); // Если type не 1 и не 2, выводим -1
    }

    return 0; // Успешное завершение программы
}
