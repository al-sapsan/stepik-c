#include <stdio.h>

int main(void)
{
    char str[100] = "Best string!";

    int write = 0; // индекс для записи результата (двигается только при копировании)
    // read - индекс для чтения исходной строки (двигается всегда вперёд)
    for (int read = 0; str[read] != '\0'; ++read) { 
        if (str[read] != 's')
            str[write++] = str[read];
    }
    str[write] = '\0'; // После цикла устанавливаем нуль-терминатор в позицию write = 10:
        /*Без явной установки '\0':
- Исходная строка: "Best string!" (12 символов + \0 = длина 13) останется в памяти
- При выводе printf("%s", str) "Bet tring!" (10 символов) будет выводиться мусор после !
- Функции вроде strlen() будут считать длину неправильно
        *Почему именно write = 10
- После цикла write содержит индекс, куда должен быть записан следующий символ
- Для строки "Bet tring!" (10 символов):
- Символ '!' записан в str[9]
- write после этого увеличивается до 10
- Поэтому '\0' ставится в str[10] */
    return 0;
}