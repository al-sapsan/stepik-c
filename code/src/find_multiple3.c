#include <stdio.h>

int main(void)
{
    int n, m; // Два числа для диапазона

    // Считываем два натуральных числа
    if (scanf("%d %d", &n, &m) != 2 || n <= 0 || m <= 0 || n >= m) {
        printf("Input error\n"); // Проверка корректности ввода
        return 0;
    }

    // Находим первое число в диапазоне [n, m], которое кратно 3
    if (n % 3 != 0) {
        n += 3 - (n % 3);
    }

    // Выводим все числа кратные 3 в диапазоне [n, m] с использованием while
    while (n <= m) {
        printf("%d ", n);
        n += 3; // Переходим к следующему числу, кратному 3
    }

    printf("\n"); // Переход на новую строку
    return 0;
}