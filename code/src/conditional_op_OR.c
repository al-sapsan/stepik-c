#include <stdio.h>

int main(void)
{
    int a, b, h; // Переменные для хранения оснований и высоты трапеции

    // Считываем три целых числа через запятую
    if (scanf("%d, %d, %d", &a, &b, &h) != 3) {
        printf("Input error.\n"); // Если ввод некорректен
        return 0;
    }

    // Проверяем, что значения находятся в диапазоне [1; 100]
    // Если хотя бы одно из условий истинно, то весь условный оператор сработает.
    if (a < 1 || a > 100 || b < 1 || b > 100 || h < 1 || h > 100) {
        printf("Values out of range.\n");
        return 0;
    }

    // Вычисляем площадь трапеции
    double area = ((a + b) / 2.0) * h;

    // Выводим результат с точностью до десятых
    printf("%.1f\n", area);

    return 0; // Успешное завершение программы
}