#include <stdio.h>

/**
 * @brief  Проверяет, является ли введённое число чётным или нечётным.
 * Основан на том, что у нечётного числа в нулевом разряде единица, 
 * а у чётного — ноль.
 * @return int Код завершения программы (0 - успешно)
 */
int main() {
    int number;
    
    // Ввод числа с проверкой корректности
    if (scanf("%d", &number) != 1) {
        printf("Input error.\n");
        return 0;
    }

    // Проверка чётности числа с помощью побитовой операции
    if (number & 1) {
        printf("NO\n");   // Нечётное
    } else {
        printf("YES\n");  // Чётное
    }
    return 0;
}