#include <stdio.h>

int main(void)
{
    int number; // Переменная для хранения неотрицательного числа

    // Считываем число из входного потока
    if (scanf("%d", &number) != 1 || number < 0) {
        printf("Input error.\n"); // Если ввод некорректен или число отрицательное
        return 0;
    }

    // Выполняем умножение на 8 с использованием сдвига влево
    int result = number << 3;

    // Выводим результат
    printf("%d\n", result);

    return 0;
}
/*в двоичном числе позиция единички (разряд) соответствует 
десятичному числу 2^n (n - это позиция). 
Например:
0d2 = 0b0000_0010 // единичка на 1-м месте (n=1), значит 2^1 = 2
0d4 = 0b0000_0100 // единичка на 2-м месте (n=2), значит 2^2 = 4
0d8 = 0b0000_10002 // единичка на 3-м месте (n=3), значит 2^3 = 8
и т.д.*/