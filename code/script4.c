/*** Includes ***/
#include <stdio.h>

/*** Main Function ***/
/**
 * @brief  Точка входа в программу
 *         Открывает файл test.dat на чтение, возвращает 0 или 2
 * @return Код завершения (0 — успешно, 2 — ошибка открытия)
 */
int main(void)
{
    FILE *fp = fopen("test.dat", "r");
    if (fp != NULL)
    {
        fclose(fp);
        return 0;
    }
    else
    {
        return 2;
    }
}
