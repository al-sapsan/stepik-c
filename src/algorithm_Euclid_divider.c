#include <stdio.h>
/*
Алгори́тм Евкли́да — эффективный алгоритм для нахождения наибольшего 
общего делителя двух целых чисел (или общей меры двух отрезков).В самом 
простом случае алгоритм Евклида применяется к паре положительных целых 
чисел и формирует новую пару, которая состоит из меньшего числа и остатка
от деления большего числа на меньшее. Процесс повторяется, пока числа не 
станут равными. Найденное число и есть наибольший общий делитель исходной пары. 
*/
int main(void)
{
    int a, b; // Два натуральных числа

    // Считываем два числа из входного потока
    if (scanf("%d %d", &a, &b) != 2 || a <= 0 || b <= 0) {
        printf("Input error\n"); // Проверка корректности ввода
        return 0;
    }

    // Реализация алгоритма Евклида
    while (b > 0) {
        int temp = b; // Сохраняем значение меньшего числа
        b = a % b;    // Остаток от деления большего на меньшее
        a = temp;     // Меньшее число становится большим
    }

    // Выводим НОД
    printf("%d\n", a);

    return 0;
}