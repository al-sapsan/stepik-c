/* Решение
1. Объявляем массив doudle mean[3]
2. Читаем каждую строку массива vls и вычисляем среднее арифметическое 
   для каждой строки
3. Записываем вычисленное значение в массив mean
4. Выводим массив mean по порядку через пробел до сотых
* Необходимые библиотеки:
- библиотека stdio.h для ввода/вывода
* Необходимые переменные:
- переменная для хранения текущего элемента массива vls
- переменная для хранения суммы элементов строки
- переменная для хранения количества элементов в строке
- переменная для хранения среднего арифметического
- переменная для хранения текущего элемента массива mean
* Необходимые структуры данных:
- два массива vls и mean
- цикл for для чтения массива vls
- цикл for для вычисления среднего арифметического
- цикл for для вывода массива mean
* Необходимые функции:
- функция scanf() для чтения массива vls
- функция для вычисления среднего арифметического
- функция printf() для вывода массива mean
*/

#include <stdio.h>

#define ROWS    3
#define COLS    5

int main(void)
{
    short vls[ROWS][COLS] = {0};
    short *ptr_vls = &vls[0][0];

    short x;
    for(int i = 0; i < ROWS * COLS && scanf("%hd", &x) == 1; ++i)
        *ptr_vls++ = x;

    // Объявляем массив для средних значений
    double mean[ROWS];

    // Вычисляем среднее арифметическое для каждой строки
    for (int i = 0; i < ROWS; ++i) {
        int sum = 0;
        for (int j = 0; j < COLS; ++j) {
            sum += vls[i][j];
        }
        mean[i] = (double)sum / COLS;
    }

    // Выводим значения mean с точностью до сотых
    for (int i = 0; i < ROWS; ++i)
        printf("%.2f ", mean[i]);

    return 0;
}