/*
Задача:
Продолжите программу. Необходимо вычислить тангенс угла прямоугольного 
треугольника с противолежащим катетом размером a и прилежащим катетом 
размером b. (Вычисляется по формуле: tan = a / b.) Затем, определите 
угол (в радианах) по вычисленному тангенсу. Выведите в консоль тангенс 
угла и угол в виде вещественных чисел с точностью до сотых в одну 
строчку через пробел.
*/
#include <stdio.h>
#include <math.h>

int main(void)
{
    int a, b;
    scanf("%d, %d", &a, &b);

    // Проверка на корректность ввода
    if (b == 0) {
        printf("Input error.\n"); // Прилежащий катет не может быть равен нулю
        return 0;
    }

    // Вычисляем тангенс угла
    double tan_value = (double)a / b;

    // Вычисляем угол в радианах по тангенсу
    double angle_radians = atan(tan_value);

    // Выводим тангенс угла и угол в радианах с точностью до сотых
    printf("%.2f %.2f\n", tan_value, angle_radians);

    return 0;
}
/*
Объяснение:
1. Считывание значений a и b:
- Программа считывает два целых числа a и b из входного потока.
- Проверяется, что b не равно нулю, так как прилежащий катет не может быть равен нулю.
2. Вычисление тангенса:
- Тангенс угла вычисляется по формуле tan = a / b, где a - противолежащий катет, b - прилежащий катет.
3. Вычисление угла в радианах:
- Угол в радианах вычисляется с помощью функции atan(), которая возвращает арктангенс (угол, соответствующий заданному тангенсу).
4. Вывод результата:
- Результат выводится с точностью до двух знаков после запятой с помощью спецификатора %.2f.
*/
// Примечание:
// - Важно помнить, что тангенс может быть неопределен (бесконечен) при b = 0, поэтому необходимо проверять это условие.
// - Функция atan() возвращает значение угла в радианах, соответствующее заданному тангенсу.
// - Для перевода радиан в градусы можно использовать формулу: degrees = radians * 180 / PI, где PI = 3.1415.
// - Для работы с математическими функциями необходимо подключить библиотеку <math.h>.