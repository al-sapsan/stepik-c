/*Задача:
Диагональ экрана телевизора составляет N дюймов. 
Выразите диагональ экрана в сантиметрах, если в одном дюйме 2,54 см. 
Результат округлите до целого числа сантиметров (по правилам математики). 
Результат выведите в консоль в виде одного целого числа.
*/
#include <stdio.h>
#include <math.h> // Для использования функции округления

int main(void)
{
    unsigned short N; // Диагональ экрана в дюймах
    scanf("%hu", &N);

    // Конвертируем диагональ в сантиметры и округляем
    double diagonal_cm = N * 2.54; // Перевод дюймов в сантиметры
    unsigned int result = (unsigned int)round(diagonal_cm); // Округление до ближайшего целого

    // Вывод результата
    printf("%u\n", result);

    return 0;
}
/* Объяснение:
1.Считывание значения N:
- Программа считывает значение диагонали экрана в дюймах (N) из входного потока.
- Используется тип unsigned short для экономии памяти, так как значение диагонали 
не может быть отрицательным.
2.Перевод в сантиметры:
- Формула N * 2.54 используется для перевода дюймов в сантиметры, так как в одном 
дюйме 2.54 см.
3.Округление:
- Функция round() из библиотеки <math.h> используется для округления значения 
до ближайшего целого числа по правилам математики.
- Результат округления приводится к типу unsigned int.
4. Вывод результата:
- Округленное значение выводится в консоль в виде целого числа.
*/