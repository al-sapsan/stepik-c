#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int main(void)
{
    srand(time(NULL)); // Инициализация генератора случайных чисел

    int a, b;
    // Считываем значения a и b
    if (scanf("%d %d", &a, &b) != 2 || a >= b) {
        printf("Input error.\n"); // Проверка корректности ввода
        return 0;
    }

    // Генерация и вывод 10 случайных чисел в диапазоне [a; b]
    for (int i = 0; i < 10; i++) {
        int random_number = rand() % (b - a + 1) + a; // Генерация числа в диапазоне [a; b]
        printf("%d ", random_number);
    }
    printf("\n"); // Переход на новую строку после вывода чисел

    return 0;
}
/*Строка int random_number = rand() % (b - a + 1) + a; используется для генерации случайного числа 
в диапазоне от a до b (включительно). 
Давайте разберем её подробно:

1. rand()
Функция rand() генерирует псевдослучайное целое число в диапазоне от 0 до RAND_MAX.
Значение RAND_MAX — это константа, определённая в стандартной библиотеке <stdlib.h>. 
Обычно она равна 32767, но может быть другой в зависимости от реализации.
2. rand() % (b - a + 1)
Оператор % (остаток от деления) используется для ограничения диапазона случайных чисел.
(b - a + 1) вычисляет количество целых чисел в диапазоне [a; b]. Например:
Если a = 3 и b = 7, то b - a + 1 = 7 - 3 + 1 = 5. Диапазон содержит числа 3, 4, 5, 6, 7 (всего 5 чисел).
rand() % (b - a + 1) возвращает случайное число в диапазоне [0; b - a]. Например:
Если b - a + 1 = 5, то результат будет одним из значений: 0, 1, 2, 3, 4.
3. + a
После вычисления остатка от деления, результат находится в диапазоне [0; b - a].
Чтобы сдвинуть этот диапазон в [a; b], прибавляется a. Например:
Если результат rand() % (b - a + 1) равен 0, то итоговое значение будет a.
Если результат равен b - a, то итоговое значение будет b.
*/