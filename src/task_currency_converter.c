#include <stdio.h>
#define DOLLAR 62.86
#define EURO 70.57
#define PLN 16.51

/**
 * @brief Конвертер валют: рубли в доллары, евро или польские злотые.
 *
 * Пользователь вводит сумму в рублях и выбирает валюту для конвертации.
 * Программа выводит результат конвертации или сообщение об ошибке.
 *
 * @return 0 при успешном завершении
 */
int main() {
    float sum;
    char v;

    printf("Введите сумму в рублях: ");
    scanf("%f", &sum);

    printf("Введите D, если хотите получить сумму в долларах США\n");
    printf("Введите E, если хотите получить сумму в евро\n");
    printf("Введите P, если хотите получить сумму в польских злотых\n");
    printf("Ваш выбор: ");
    scanf(" %c", &v); // пробел перед %c для пропуска пробелов/переводов строки

    switch (v) {
        case 'D':
        case 'd':
            printf("Вам следует получить %.2f $\n", sum / DOLLAR);
            break;
        case 'E':
        case 'e':
            printf("Вам следует получить %.2f EUR\n", sum / EURO);
            break;
        case 'P':
        case 'p':
            printf("Вам следует получить %.2f PLN\n", sum / PLN);
            break;
        default:
            printf("Неверный выбор\n");
    }

    return 0;
}