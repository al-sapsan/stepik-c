#include <stdio.h>
#include <math.h>

int main() {
    int number; // Переменная для хранения введенного числа

    // Считываем целое число из входного потока
    if (scanf("%d", &number) != 1 || number <= 0) {
        // Проверяем, что ввод корректен и число положительное
        printf("Input error.\n");
        return 0; // Завершаем программу в случае ошибки
    }

    // Вычисляем количество цифр в числе минус 1
    // log10(number) возвращает десятичный логарифм числа, 
    // (int)log10(number) преобразует результат в целое число
    int digits = (int)log10(number);

    // Вычисляем первую цифру числа
    // Делим число на 10 в степени digits, чтобы получить первую цифру
    int first_digit = number / (int)pow(10, digits);

    // Проверяем, равна ли первая цифра числу 3
    // Если равна, выводим "yes", иначе "no"
    printf(first_digit == 3 ? "yes" : "no");

    return 0; // Успешное завершение программы
}