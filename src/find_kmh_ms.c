#include <stdio.h>

/**
 * @brief Сравнивает две скорости: одну в км/ч, другую в м/с.
 *
 * Пользователь вводит две скорости через пробел. Если хотя бы одна отрицательная — "Ошибка".
 * Если первая скорость больше — "Больше", если меньше — "Меньше", если равны — "Равны".
 *
 * @return 0 при успешном завершении
 */
int main() {
    double v_kmh, v_ms;
    if (scanf("%lf %lf", &v_kmh, &v_ms) != 2 || v_kmh < 0 || v_ms < 0) {
        printf("Ошибка\n");
        return 0;
    }

    // Переводим первую скорость в м/с
    double v1 = v_kmh / 3.6;
    double v2 = v_ms;

    if (v1 > v2)
        printf("Больше\n");
    else if (v1 < v2)
        printf("Меньше\n");
    else
        printf("Равны\n");

    return 0;
}